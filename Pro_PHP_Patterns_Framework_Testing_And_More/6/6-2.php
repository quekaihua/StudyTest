/**
 * 这个函数连接了两个字符串
 *
 * 通过第一个字符串，并将它添加到第二个字符串上，并返回结果
 * 它完成了这一功能
 *
 * @param $string1  左边的字符串
 * @param $string2  右边的字符串
 * @return string   包含了合并后内容的字符串
 * @todo 添加一个连接字符参数
 */
function concatTowStrings($string1,$string2){
    return $string1.$string2;
}